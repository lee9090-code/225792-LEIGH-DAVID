// This program demonstrates the use of various data types in C++
#include <iostream>
using namespace std;
int main() {
    // Integer data type
    int intVar = 42; //Integer variable to store whole numbers
    cout << "Integer Variable: " << intVar << endl;

    // Floating-point data type
    float floatVar = 3.14; // Float variable to store decimal numbers
    cout << "Float Variable: " << floatVar << endl;

    // Double data type
    double doubleVar = 2.718281828459; // Double variable for higher precision decimal numbers
    cout << "Double Variable: " << doubleVar << endl;

    // Character data type
    char charVar = 'A'; // Character variable to store single characters
    cout << "Character Variable: " << charVar << endl;

    // Boolean data type
    bool boolVar = true; // Boolean variable to store true/false values
    cout << "Boolean Variable: " << boolVar << endl;

    return 0;
}   

